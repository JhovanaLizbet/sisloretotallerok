<?php

class Instructor_model extends CI_Model
{

	//////////////////////////////////////////////////////////////////////////////////////////////////	
	/////////////////////////// AHORA REALIZAMOS LAS /////////////////////////////////////////////////	
	/////////////////////////// CONSULTAS  SQL  PARA /////////////////////////////////////////////////	
	/////////////////////////// REALIZAR  LOS  CRUD  /////////////////////////////////////////////////	
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function listaInstructores() //recupera la lista de todos los instructores
	{
		$this->db->select('*');

		$this->db->from('instructor');
		$this->db->where('estado', '1'); //muestra solo est hab
		return $this->db->get();
	}
	public function listaInstructoresCombo()
    {
        $this->db->SELECT('id,
                           CONCAT(nombres," ",primerApellido," ",IFNULL(segundoApellido,"")) AS instructorname,
                           fechaNacimiento,
                           sexo,
                           telefono,
                           email,
                           nombreUsuario,
                           rol,
                           fechaRegistro,
                           idUsuario');
        $this->db->FROM('instructor');
        $this->db->WHERE('estado', '1');
        return $this->db->get();
    }


	public function listaInstructoresDeshabilitados() //recupera la lista de todos los usuarios
	{
		$this->db->select('*');
		$this->db->from('instructor');
		$this->db->where('id <>', $this->session->userdata('idInstructor'));
		$this->db->where('estado', '0'); //muestra solo est hab
		return $this->db->get();
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////	
	//////////////////////////////////// I N S E R T /////////////////////////////////////////////////	
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function agregarInstructor($data)
	{
		$this->db->insert('instructor', $data); //inserta a la base de datos
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////	
	//////////////////////////////////// RECUPERAMOS /////////////////////////////////////////////////	
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function recuperarDatosInstructor($idInstructor)
	{
		$this->db->select('*');
		$this->db->from('instructor');
		$this->db->where('id', $idInstructor);
		return $this->db->get();
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////	
	//////////////////////////////////// MODIFICAMOS /////////////////////////////////////////////////	
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function modificarInstructor($idInstructor, $data)
	{
		$this->db->where('id', $idInstructor);
		$this->db->update('instructor', $data);
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////	
	//////////////////////////////////// ELIMINAMOS //////////////////////////////////////////////////
	///////////////////////////////// DESHABILITAMOS /////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function deshabilitarInstructor($idInstructor, $data) //recupera la lista de todos los estudiantes
	{
		// BD
		$this->db->where('id', $idInstructor);
		$this->db->update('instructor', $data);
	}
//////////////////////////////////////////////////////////////////////////////////////////////////	
	////////////////////////////////// HABILITAMOS ///////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function habilitarInstructor($idInstructor, $data) //recupera la lista de todos los estudiantes
	{
		// BD
		$this->db->where('id', $idInstructor);
		$this->db->update('instructor', $data);
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////	
	//////////////////////////////////////////////////////////////////////////////////////////////////	
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function verificarNombreInstructor($nombreUsuario)
	{
		$this->db->SELECT('*');
		$this->db->FROM('instructor');
		$this->db->WHERE('nombreUsuario', $nombreUsuario);
		return $this->db->get();
	}
}
