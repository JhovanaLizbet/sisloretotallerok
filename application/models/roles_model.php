<?php

class Roles_model extends CI_Model
{

	//////////////////////////////////////////////////////////////////////////////////////////////////	
	/////////////////////////// AHORA REALIZAMOS LAS /////////////////////////////////////////////////	
	/////////////////////////// CONSULTAS  SQL  PARA /////////////////////////////////////////////////	
	/////////////////////////// REALIZAR  LOS  CRUD  /////////////////////////////////////////////////	
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function obtenerRoles($nombre) 
	{
        $query = $this->db->get_where('rol', array('nombre' => $nombre));
        $row = $query->row();
        return $row->id;
    }

	public function listaRoles() //recupera la lista de todos los clientes
	{
		$this->db->select('*');
		$this->db->from('rol');
		$this->db->where('estado', '1'); //muestra solo est hab
		return $this->db->get();
	}
	public function listaRolesDeshabilitados() //recupera la lista de todos los usuarios
	{
		$this->db->select('*');
		$this->db->from('rol');
		$this->db->where('id <>', $this->session->userdata('idRol'));
		$this->db->where('estado', '0'); //muestra solo est hab
		return $this->db->get();
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////	
	//////////////////////////////////// I N S E R T /////////////////////////////////////////////////	
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function agregarRol($data)
	{
		$this->db->insert('rol', $data); //inserta a la base de datos
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////	
	//////////////////////////////////// RECUPERAMOS /////////////////////////////////////////////////	
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function recuperarDatosRoles($idRol)
	{
		$this->db->select('*');
		$this->db->from('rol');
		$this->db->where('id', $idRol);
		return $this->db->get();
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////	
	//////////////////////////////////// MODIFICAMOS /////////////////////////////////////////////////	
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function modificarRoles($idRol, $data)
	{
		$this->db->where('id', $idRol);
		$this->db->update('rol', $data);
	}

	//////////////////////////////////////////////////////////////////////////////////////////////////	
	//////////////////////////////////// ELIMINAMOS //////////////////////////////////////////////////
	///////////////////////////////// DESHABILITAMOS /////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function deshabilitarRoles($idRol, $data) //recupera la lista de todos los estudiantes
	{
		// BD
		$this->db->where('id', $idRol);
		$this->db->update('rol', $data);
	}
//////////////////////////////////////////////////////////////////////////////////////////////////	
	////////////////////////////////// HABILITAMOS ///////////////////////////////////////////////////
	//////////////////////////////////////////////////////////////////////////////////////////////////	

	public function habilitarRoles($idRol, $data) //recupera la lista de todos los estudiantes
	{
		// BD
		$this->db->where('id', $idRol);
		$this->db->update('rol', $data);
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////	
	//////////////////////////////////////////////////////////////////////////////////////////////////	
	//////////////////////////////////////////////////////////////////////////////////////////////////	

}
